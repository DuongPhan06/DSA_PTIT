int a[1000001];
void Merge(int a[], int l, int m, int r)
{
    vector<int> x(a + l, a + m + 1);
    vector<int> y(a + m + 1, a + r + 1);
    int i = 0, j = 0;
    while (i < x.size() && j < y.size())
    {
        if (x[i] <= y[j])
        {
            a[l] = x[i];
            l++;
            i++;
        }
        else
        {
            a[l] = y[j];
            l++;
            j++;
        }
    }
    while (i < x.size())
    {
        a[l] = x[i];
        l++;
        i++;
    }
    while (j < y.size())
    {
        a[l] = y[j];
        l++;
        j++;
    }
}
void mergeSort(int a[], int l, int r)
{
    if (l >= r)
    {
        return;
    }
    int p = (l + r) / 2;
    mergeSort(a, l, p);
    mergeSort(a, p + 1, r);
    Merge(a, l, p, r);
}

